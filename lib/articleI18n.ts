export function localizeArticle<
  T extends { slug?: string; title?: string; excerpt?: string | null; body?: string }
>(article: T, locale: "ru" | "jp"): T {
  const tr = article?.slug ? ARTICLE_I18N[article.slug]?.[locale] : undefined
  if (!tr) return article
  return { ...article, title: tr.title, excerpt: tr.excerpt, body: tr.body }
}
